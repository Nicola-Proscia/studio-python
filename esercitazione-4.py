#ciclo FOR esegue il codice per un numero definito di volte

for number in range(11):  #for è il ciclo \ number è il nome della variabile \ in parola chiave  \ range è il nome della funzione che prende un range di numeri in questo caso 11
    print(number)
    
    

for number in range(0, 12, 2):  #possiamo creare un cilco con allingterno del range 3 valori che si suddividono in (start, stop, step) 
    print(number)               #lo start = numero di inzio \ lo stop = numero di fine \ lo step = andatura del cilco 
    
    
for number in range(2, 22, 3):
    print(number)  
    
for number in range(0, 100, 10):
    print(number)  
    